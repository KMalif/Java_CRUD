    public void nipPegawai(){
        try {
            Statement st = (Statement)Koneksi.koneksiDB().createStatement();
            rs = st.executeQuery("SELECT * FROM data_pegawai ORDER BY nip DESC");
            if(rs.next()){
                String nipPegawai = rs.getString("nip").substring(1);
                String AN = "" +(Integer.parseInt(nipPegawai) +1);
                String Nol = "";
                
                if(AN.length()==1){Nol = "000";}
                if(AN.length()==2){Nol = "00";}
                if(AN.length()==3){Nol = "0";}
                if(AN.length()==4){Nol = "";}
                
                txtNip.setText("P" + Nol + AN);
            }else{
                txtNip.setText("P0001");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);

        }   
    }
    
    public void getData(){
        try {
            Statement st = (Statement)Koneksi.koneksiDB().createStatement();
            rs = st.executeQuery("SELECT * FROM data_pegawai");
            
            while(rs.next()){
                Object[] obj = new Object[7];
                obj[0] = rs.getString("NIP");
                obj[1] = rs.getString("Nama");
                obj[2] = rs.getString("Tempat_Lahir");
                obj[3] = rs.getString("Tanggal_Lahir");
                obj[4] = rs.getString("Status");
                obj[5] = rs.getString("Jabatan");
                obj[6] = rs.getString("Pendidikan");
                
                model.addRow(obj);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }

    public void cleanData(){
        while(model.getRowCount()>0){
            for(int a=0 ; a<model.getRowCount(); a++){
                model.removeRow(0);
            }
        }
    }
    
