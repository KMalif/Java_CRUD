            String sql = "INSERT INTO data_pegawai values ('"+txtNip.getText()+"','"+txtNama.getText()+"','"+txtTempatLahir.getText()+"',"
                    + "'"+newTanggalLahir+"','"+cmbStatus.getSelectedItem()+"','"+cmbJabatan.getSelectedItem()+"','"+cmbPendidikan.getSelectedItem()+"')";
            Connection con = (Connection)Koneksi.koneksiDB();
            PreparedStatement pst = con.prepareStatement(sql);
            pst.execute();
